// ============== Hoisting ==============
// Hoisting is a JavaScript mechanism where 
// variable and function declarations are 
// conceptually moved to the top of their 
// scope during the compilation phase, 
// before code execution. This allows functions
// to be called before they are defined 
// and variables to be referenced 
// (as undefined) before declaration


